// Создаем массив с днями недели
const daysOfWeek = ['Понедельник', 'Вторник', 'Среда', 'Четверг', 'Пятница', 'Суббота', 'Воскресенье'];
let currentDayIndex = 0;

//Используем бесконечный цикл while, который прерывается только когда пользователь нажимает "Отмена"
while (true) {
    //Каждый раз показываем текущий день недели и запрашиваем подтверждение для перехода к следующему
    const userResponse = confirm(`${daysOfWeek[currentDayIndex]}. Хотите увидеть следующий день?`);
    
    if (!userResponse) {
        break; // Прерываем цикл, если пользователь нажал "Отмена"
    }

    //При достижении конца массива дней недели, индекс сбрасывается на 0 (воскресенье → понедельник)
    currentDayIndex = (currentDayIndex + 1) % daysOfWeek.length; // Переходим к следующему дню с зацикливанием
}