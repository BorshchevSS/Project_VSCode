alert("Загадайте число от 0 до 100. Я попробую угадать его!");

//Программа начинает с диапазона 0-100
let min = 0;
let max = 100;
let guess;
let attempts = 0;

while (min <= max) { //Процесс повторяется, пока не будет получен ответ "="
    attempts++;
    guess = Math.floor((min + max) / 2); //Каждый раз она берет середину текущего диапазона (Math.floor((min+max)/2))
    
    const answer = prompt(`Ваше число > ${guess}, < ${guess} или == ${guess}? (Введите ">", "<" или "=")`); //Спрашивает пользователя, его число больше, меньше или равно предложенному
    
    //В зависимости от ответа сужает диапазон:
    //- Если ">" - новый диапазон (guess+1)..max
    //- Если "<" - новый диапазон min..(guess-1)
    if (answer === "=") {
        alert(`Ура! Я угадал ваше число ${guess} за ${attempts} попыток!`);
        break;
    } else if (answer === ">") {
        min = guess + 1;
    } else if (answer === "<") {
        max = guess - 1;
    } else {
        alert('Пожалуйста, введите ">", "<" или "="');
    }
}

//Если min становится больше max - значит, пользователь где-то ошибся в ответах
if (min > max) {
    alert("Кажется, вы где-то ошиблись в ответах. Давайте попробуем снова!");
}

//Программа гарантированно угадает число за максимум 7 попыток (log₂100 ≈ 6.64).